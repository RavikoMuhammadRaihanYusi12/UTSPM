public class ReverseArray2 {
    public static void main(String[] args) {
        int arry2[] = {6, 15, 19, 23, 28, 32, 45};
        System.out.println("Reverse Order Array 2 :");
        for (int i = arry2.length - 1; i >= 0; i--) {
            System.out.println(arry2[i] + "");
        }
    }
}
